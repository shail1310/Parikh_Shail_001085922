    private void populateTable(ArrayList<VitalSign> list) {
       DefaultTableModel dtm = (DefaultTableModel) vitalSignstbl.getModel();
       dtm.setRowCount(0);
       for (VitalSign vs : list) {
           Object row[] = new Object[2];
           row[0] = vs;
           row[1] = vs.getBloodPressure();
           dtm.addRow(row);
       }
   }
    
    private void setDefaultTable(double max, double min) {
       list = vsh.getAbnormalList(max, min);
       initialTextFields();
       populateTable(list);
   }
   private void initialTextFields(){
       txtTemperatureView.setText("");
       txtBloodPressureView.setText("");
       txtPulseView.setText("");
       txtDateView.setText("");
   }

