        // TODO add your handling code here:
                   int selectedRow = vitalSignstbl.getSelectedRow();
                   
                   if(selectedRow>=0){
                       VitalSign vs = (VitalSign) vitalSignstbl.getValueAt(selectedRow, 0);
                       txtTemperatureView.setText(vs.getTemperature()+ "");
                       txtTemperatureView.setEnabled(false);
                       
                       txtBloodPressureView.setText(vs.getBloodPressure()+ "");
                       txtBloodPressureView.setEnabled(false);
                       txtDateView.setText(vs.getDate()+ "");
                       txtDateView.setEnabled(false);
                       txtPulseView.setText(vs.getPulse()+ "");
                       txtPulseView.setEnabled(false);
                   }
                   else {
                       JOptionPane.showMessageDialog(null,"Pease select a row");
                   } 
