    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedrow= tblVitalSigns.getSelectedRow();
        
        if(selectedrow>=0){
            VitalSign vs = (VitalSign)tblVitalSigns.getValueAt(selectedrow,0);
            txtBloodPressureView.setText(String.valueOf(vs.getBloodPressure()));
            txtTemperatureView.setText(String.valueOf(vs.getTemperature()));
            txtPulseView.setText(String.valueOf(vs.getPulse()));
            txtDateView.setText(String.valueOf(vs.getDate()));
                       
        }
        else 
            JOptionPane.showMessageDialog(null,"Please Select A Row");
    }//GEN-LAST:event_btnViewDetailsActionPerformed

