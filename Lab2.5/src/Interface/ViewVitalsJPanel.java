    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedrow= tblVitalSigns.getSelectedRow();
        
        if(selectedrow>=0){
            VitalSign vsu = (VitalSign)tblVitalSigns.getValueAt(selectedrow,0);
            
            
        double temperature= Double.parseDouble(txtTemperatureView.getText());
        double bp= Double.parseDouble(txtBloodPressureView.getText());
        int pulse= Integer.parseInt(txtPulseView.getText());
        String date= txtDateView.getText();
        
        
        vsu.setBloodPressure(bp);
        vsu.setTemperature(temperature);
        vsu.setDate(date);
        vsu.setPulse(pulse);
        JOptionPane.showMessageDialog(null, "Vital Signs Updated Successfully");
        
        txtBloodPressureView.setText("");
        txtDateView.setText("");
        txtPulseView.setText("");
        txtTemperatureView.setText("");
            
            
            
            populateTable();
        
        }
        else 
            JOptionPane.showMessageDialog(null,"Please Select A Row");
        
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

